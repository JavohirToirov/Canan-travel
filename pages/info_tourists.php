               <p>Foreign citizens in transit through the Republic of Uzbekistan, to obtain a transit visa for a period of <strong>72 hours</strong>, must submit to <strong>the diplomatic mission or Consulate of the Republic of Uzbekistan</strong> in the country of their stay <strong>the entire list of documents necessary for the tourist visa</strong>, as well as attach to it:</p>
               <ul>
                 <li>Visa of the destination country;</li>
                 <li>Travel documents with confirmed date of departure from Uzbekistan to the destination country;</li>
               </ul>
               <p>The submitted documents are considered within <strong>3 working days.</strong></p>
