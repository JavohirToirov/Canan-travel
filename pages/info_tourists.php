               <h2>Visa To Uzbekistan</h2>
               <p>Residents of <strong>the above States</strong> also have the opportunity to obtain an electronic tourist visa by filling out a questionnaire on the <strong>E-VISA</strong> portal and paying a fee of <strong>20 USD</strong>. E-visa is sent in the PDF format on the e-mail of the applicant and is not pasted into the passport.</p>
