         <div class="objects-mainBlock-content">
             <div class="objects-mainBlock-content-box">
                 <p> In Uzbekistan, the main tourist centers of which – the cities of Samarkand, Bukhara and Khiva – are included in the UNESCO World Heritage List, there are hundreds of historical  architectural monuments that still remember the majestic tread of caravans from the times of the Great Silk Road. However, the sights of Uzbekistan are not only monumental madrasahs and  slender minarets, mausoleums of rulers and spiritual leaders.
                 </p>
                 <p>The nature of Uzbekistan, its traditions, culture, cuisine and crafts are surprisingly diverse.</p>
                 <p>Folk crafts of Uzbekistan are worthy of a separate story: silk fabrics and carpets, bright ceramics and sharp knives, woodcarving and metal embossing, lacquer miniatures and gold embroidery, famous suzani and ancient styles of embroidery “Basma” and “Iroki”, paper making according to ancient technologies and construction of earthquake-resistant clay houses.</p>
             </div>
             <div class="objects-mainBlock-content-box">
                 <p>And the Uzbek cuisine, which, by the behest of fate, found itself at the crossroads of the culinary preferences of civilizations and skillfully absorbed all the best for many centuries and without which a conversation about the sights of Uzbekistan is simply impossible? Pilaf, cakes, sweets – the list of Uzbek dishes is endlessly delicious. And besides, there is a specialization in dishes: here are pilaf centers, there are lagman centers, barbecue centers and samosyshny ones at a distance. In every city there are many teahouses, quails are singing in every teahouse, huge yellow honey-sweet melons are cooled in the cool waters of ditches, and peaches, pomegranates and transparent grapes ripen in the gardens.
                 </p>
                 <p>The natural attractions of Uzbekistan are such that you can practice mountain skiing in winter, take ecological camel tours in the desert in spring, swim in a mountain lake and paraglide in the Chimgan mountains in summer, or walk along the bottom of the drying Aral Sea in Karakalpakstan in autumn.</p>
                 <p>Be sure to take the time to visit museums: the Museum. Savitsky in Nukus, the local history museum of Kokand, the Afrosiab museum in Samarkand, the house-museum of Faizulla Khodjaev in Bukhara, the museum of steam locomotives in Tashkent. There are dozens of them – choose according to your interests.</p>
