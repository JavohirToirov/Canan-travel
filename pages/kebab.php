                    <p>
                        You even think that you understand everything and are ready to cook a dish on their own, but it is not so. Shish kebab still gets another taste, so do not waste time on studying the recipe. Just try the barbecue, and free time is better to pay local attractions.
                    </p>
                    <p>
                       How to cook kebab “Three fingers”
                       <br>
                       Do not worry, when cooking fingers are not used. The recipe got its name because the meat is strung on three skewers. It’s an ancient recipe, by the way. Not to carry in the field, skewers, Uzbek herdsmen strung the meat on the fork and roasted it.
                    </p>
                    <p>
                       You can prepare a dish of lamb ribs or beef pulp. To cook a barbecue for 5 people, you will need:
                    </p>
                    <ul class="kebab-item-ingridents">
                        <li>1-1.5 kg of meat;</li>
                        <li>300 gr of broadtail fat;</li>
                        <li>300 gr onion;</li>
                        <li>5 gr coriander;</li>
                        <li>100 ml of purified water;</li>
                        <li>25 g salt;</li>
                        <li>3 gr ground pepper.</li>
                    </ul>
                    <p>
                       The first step is to cut off the meat veins, cut it into slices across the fibers. Next, the meat is slightly beaten off and put in a bowl (in which there is a marinade).
                    </p>
                    <p>
                       The next step is to prepare the marinating. To do this, grind the spices and salt in a mortar, cut the onion. All this is added to the dishes to the meat and pour carbonated water. Leave the meat in the marinade for an hour.
                    </p>
                    <p>
                       <img lazy="loading" width="100%" src="/image/blog_page/meals/shashlik-tri-palca-300x167.jpg" alt="">
                    </p>
                    <p>
                       Now cut the fat tail, and begin to string it on a skewer, alternating with pieces of meat. It remains to light a fire and fry the meat at an average temperature.
                    </p>
                    <p>
                      Beef kebab
                       <br>
                      Skewers of beef tenderloin turn soft and quickly prepared. First of all, the taste of barbecue depends on the freshness of meat, so do not be lazy and go to the market for fresh.
                    </p>
