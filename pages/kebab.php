                    <p>
                        Kebab is one of the oldest dishes in the world. After all, once people killed mammoths and roasted meat on the fire. The dish is known all over the world and in each country is prepared according to a unique recipe.
                    </p>
                    <p>
                        Let’s understand what is special about Uzbek barbecue and how to cook it.
                    </p>
                    <p>
                        <strong>What is the difference between Uzbek shashlik and dishes of other countries?</strong>
                        <br>According to Uzbek national cuisine, shashlik is made from lamb. But if you have beef, pork or chicken at hand — you can experiment. The correct name of the dish is “kabob”, but more often it is called “kebab”.
                    </p>
                    <p>
                      Kebabs are prepared only by men, and women do not trust such work. First of all, the masters burn the fire for about 40 minutes, until a layer of gray ash appears and only then prepare the dish. Previously, as firewood was used branches of saxaul (a desert is a small tree originally from Central Asia), but today most chefs use conventional charcoal. It is not recommended to use coniferous wood: they can spoil the taste of meat.
                    </p>
                    <p>
                        <strong>Features of the marinade</strong>
                        Just take the meat of a young lamb and fry it — half the battle. One of the main components in the preparation of kebab — especially marinade. In Uzbekistan, Marinovka takes place in ordinary water. And this is not a typo: indeed, the meat is marinated in ordinary drinking water, sometimes — in mineral water. In a small amount of water add salt, pepper, spices (red pepper, wine vinegar, lemon juice, coriander) and sliced onion rings.
                    </p>
                    <p>
                      <img lazy="loading" width="100%" src="/image/blog_page/meals/marinad-dlya-shashlika-300x167.jpg" alt="">
                    </p>
                    <p>
                       The marinade is a real field for experiments. You can add absolutely any spices, but the main thing — do not overdo it.
                    </p>
                    <p>
                       The taste of Uzbek kebab depends on three factors: meat, marinade and skill of the cook. This master closely monitors the process, does not allow the meat to burn and extinguishes the open flame in time
                    </p>
                    <p>
                        <strong>Types of Uzbek kebab</strong>
                        Masters prepare up to 20 varieties of barbecue. If you go on a tour to Uzbekistan for the may holidays, be sure to try the Uzbek barbecue:
                    </p>
                    <ul class="kebab-item-ingridents">
                        <li>kiyma kabob (skewers of ground meat);</li>
                        <li>jigar kabob (liver kebab);</li>
                        <li>carve kabob (the meat is covered with interior fat).</li>
                    </ul>
                    <p>
                       It is also worth trying a local delicacy — skewers of quail. The cooks themselves also like to experiment, so they use not only young lamb, but also chicken, veal, pork, and even fish.
                    </p>
                    <p>
                      Where to try the best barbecue in Uzbekistan
                      <br>
                      There is a small town in Bukhara region — Gijduvan. It is not difficult to get here: the city is located 60 km from Bukhara. It is here that the best Uzbek barbecue is prepared.
                    </p>
                    <p>
                       Be sure to go there and try the local barbecue, but do not even try to learn the recipe for cooking. No, the local chefs are friendly: they will be long and painful to tell the details of the marinade and barbecue.
                    </p>
                    <p>
                       <img lazy="loading" width="100%" src="/image/blog_page/meals/prigotovleniye-shashlikov-na-mangale-300x167.jpg" alt="">
