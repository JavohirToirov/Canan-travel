              <div class="uzbekistan-mainBlock-content-text-box">
                  <h5>Date of foundation</h5>
                  <span>In its present form, Uzbekistan was formed on October 27, 1924 as the Uzbek USSR. Prior to this, the territory of Uzbekistan was divided into the Bukhara Emirate, Khorezm and the Kokand Khanate (from the 16th to the 20th century).</span>
