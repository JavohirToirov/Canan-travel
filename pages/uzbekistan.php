              <div class="uzbekistan-mainBlock-content-text-box">
                  <h5>Location</h5>
                  <span>The republic is located in the very center of Central Asia and is adjacent to Kyrgyzstan, Kazakhstan, Turkmenistan, Afghanistan and Tajikistan.</span>
              </div>
              <div class="uzbekistan-mainBlock-content-text-box">
                  <h5>Area</h5>
                  <span>Uzbekistan covers an area of ​​448,924 square kilometers, over 95% of which is land. The country is landlocked.</span>
              </div>
              <div class="uzbekistan-mainBlock-content-text-box">
                  <h5>Capital</h5>
                  <span>Since 1930, the capital of Uzbekistan is the city of Tashkent. This status was transferred to the city by the ancient and beautiful Samarkand.</span>
              </div>
              <div class="uzbekistan-mainBlock-content-text-box">
                  <h5>Population</h5>
                  <span>As of April 1, 2021, 34 695 553 people live in the country. Of these, 71.4% are Uzbeks, 8.4% are Russians, 4.7% are Tajiks, 4.1% are Kazakhs, 2.4% are Tatars, 9% are representatives of other nationalities.</span>
              </div>
              <div class="uzbekistan-mainBlock-content-text-box">
                  <h5>Official language</h5>
                  <span>The official state language of the republic is Uzbek.</span>
